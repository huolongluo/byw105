public class CoinDetailList {
    private int trademapping;
    private String cointype;
    private String fb;
    private String price;
    private double rose;
    private String isSelf;

    public int getTrademapping() {
        return trademapping;
    }

    public void setTrademapping(int trademapping) {
        this.trademapping = trademapping;
    }

    public String getCointype() {
        return cointype;
    }

    public void setCointype(String cointype) {
        this.cointype = cointype;
    }

    public String getFb() {
        return fb;
    }

    public void setFb(String fb) {
        this.fb = fb;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public double getRose() {
        return rose;
    }

    public void setRose(double rose) {
        this.rose = rose;
    }

    public String getIsSelf() {
        return isSelf;
    }

    public void setIsSelf(String isSelf) {
        this.isSelf = isSelf;
    }
}
