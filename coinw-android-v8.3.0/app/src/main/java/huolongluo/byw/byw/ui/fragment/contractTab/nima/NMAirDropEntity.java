package huolongluo.byw.byw.ui.fragment.contractTab.nima;

public class NMAirDropEntity {

    /**
     * code : 0
     * data : false
     * i18nMsgCode :  null
     * msg :  null
     * success : true
     */

    private String code;
    private boolean data;
    private String i18nMsgCode;
    private String msg;
    private boolean success;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public boolean isData() {
        return data;
    }

    public void setData(boolean data) {
        this.data = data;
    }

    public String getI18nMsgCode() {
        return i18nMsgCode;
    }

    public void setI18nMsgCode(String i18nMsgCode) {
        this.i18nMsgCode = i18nMsgCode;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }
}
